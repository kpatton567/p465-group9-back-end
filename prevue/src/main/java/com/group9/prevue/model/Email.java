package com.group9.prevue.model;

public class Email {
private String email;
private String test;
	
	public Email(String email,String test) {
		this.email = email;
		this.test = test;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTest() {
		return test;
	}

	public void setTest(String test) {
		this.test = test;
	}

}
